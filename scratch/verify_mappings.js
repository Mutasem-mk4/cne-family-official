const check = (list, label) => {
  console.log(`\nChecking ${label}...`);
  let missing = 0;
  list.forEach(([name, x, y, w, h]) => {
    // Check if name is in SUBJECT_NAME_MAP first (if mapped from visual to db name)
    // Wait! Or check if we should map from db name to visual name?
    // In main.js, subjectsByName uses db name (subject.name).
    // And hotspots array uses visual name, which is mapped via SUBJECT_NAME_MAP[visualName] -> dbName.
    // Let's see if the name we have matches a subject.name directly or through SUBJECT_NAME_MAP.
    const mapped = SUBJECT_NAME_MAP[name] || name;
    const found = subjects.find(s => s.name === mapped);
    if (!found) {
      // Also try reverse lookup just in case
      const reverseKey = Object.keys(SUBJECT_NAME_MAP).find(k => SUBJECT_NAME_MAP[k] === name);
      if (reverseKey) {
        console.log(`✅ MATCHED REVERSE: "${name}" maps from visual "${reverseKey}"`);
      } else {
        console.log(`❌ NOT FOUND: "${name}" (mapped to: "${mapped}")`);
        missing++;
      }
    } else {
      console.log(`✅ FOUND: "${name}" -> "${mapped}" (link: ${found.link})`);
    }
  });
  console.log(`Result: ${list.length - missing} / ${list.length} found.`);
};

check(commonMapping, 'Common');
